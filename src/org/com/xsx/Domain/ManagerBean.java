package org.com.xsx.Domain;

public class ManagerBean {
	private String account;
	private String password;
	private String devicelist;
	private String fatheraccount;
	private String name;
	private String age;
	private String sex;
	private String phone;
	private String job;
	private String dsctext;
	
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getDevicelist() {
		return devicelist;
	}
	public void setDevicelist(String devicelist) {
		this.devicelist = devicelist;
	}
	public String getFatheraccount() {
		return fatheraccount;
	}
	public void setFatheraccount(String fatheraccount) {
		this.fatheraccount = fatheraccount;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	public String getDsctext() {
		return dsctext;
	}
	public void setDsctext(String dsctext) {
		this.dsctext = dsctext;
	}

}
